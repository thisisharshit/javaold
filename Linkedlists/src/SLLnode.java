
public class SLLnode {

	private int data;
	private SLLnode next;
	public SLLnode(int data) {
		this.data=data;
	}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public SLLnode getNext() {
		return next;
	}
	public void setNext(SLLnode next) {
		this.next = next;
	}
	
}
